package com.akgcloud.moviecatalogservice.model;

public class MovieInfo {

	private Movie movie;
	private int rating;

	public Movie getMovie() {
		return movie;
	}

	public void setMovie(Movie movie) {
		this.movie = movie;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

}
